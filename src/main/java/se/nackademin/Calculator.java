class Calculator {

    public int add(int i, int j) {
       
        
        return i+j;
    }

    public double multiply(int i, int j) {
        
        return i * j;
    }

    double divide(int i, int j) {
        
        return i / j;
        
    }

    double subtract(int i, int j) {
        
        return i - j;
    }
    
}
